org  0x100               ; Вказуємо, що це програма .COM
section .data
    a db 5               ; Змінна a
    b db 10              ; Змінна b
    c db 6               ; Змінна c
    resultMsg db 'Result: $' ; Визначення рядка для виведення результату

; b - c + a
section .text
_start:

    ; Завантажуємо значення з [b] у регістр al
    mov al, [b]           ; Копіюємо значення з [b] у регістр al
    
    ; Виконуємо операцію b - c + a:
    sub al, [c]           ; Віднімаємо значення з [c] з регістра al (b - c)
    add al, [a]           ; Додаємо значення з [a] до регістра al (b - c + a)

    ; Перетворення результату в ASCII символ (для однозначних чисел)
    add al, 30h           ; Перетворюємо число в ASCII символ
    ; 30h - це ASCII код для символу '0'. 31h - '1', 32h - '2', ..., 39h - '9'.

    ; Виведення результату
    mov ah, 09h           ; Функція DOS для виведення рядка
    lea dx, resultMsg     ; Встановлюємо DX на адресу resultMsg
    int 21h               ; Виклик DOS-переривання

    ; Виведення числа
    mov dl, al            ; Завантажуємо результат у DL для виведення
    mov ah, 02h           ; Функція DOS для виведення символу
    int 21h               ; Виклик DOS-переривання

    ; Завершення програми
    mov ax, 4c00h         ; Функція DOS для завершення програми
    int 21h               ; Виклик DOS-переривання
